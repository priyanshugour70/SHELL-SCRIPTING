#!/bin/bash

# For Loop ..!


# For Basic Number's 
for num in 1 2 3 4 5 
do
	echo "Number is $num "
done



# For String's .. 
for str in "Priyanshu" "Devanshu" "Suhani" "Harshita"
do 
	echo "Name is : $str "
done



# For Number's in Range...
for i in {1..10}
do
	echo "Number : $i "
done



# For String Again.. 
for task in Read Write Blog Video Sleep Eat
do
	echo "My tasks is : $task"
done




